def chunk_text(text: str, chunk_size: int = 800, overlap: int = 120) -> list[str]:
    """
    Replicate the chunking strategy from vector_store.py:
    RecursiveCharacterTextSplitter(chunk_size=800, chunk_overlap=120).
    """
    separators = ["\n\n", "\n", ". ", " "]
    chunks = []

    def _split(text, seps):
        if not seps or len(text) <= chunk_size:
            return [text] if text.strip() else []
        sep = seps[0]
        parts = text.split(sep)
        result = []
        current = ""
        for part in parts:
            candidate = current + sep + part if current else part
            if len(candidate) <= chunk_size:
                current = candidate
            else:
                if current:
                    result.append(current)
                if len(part) > chunk_size:
                    result.extend(_split(part, seps[1:]))
                else:
                    current = part
        if current:
            result.append(current)
        return result

    raw_chunks = _split(text, separators)

    # Apply overlap: prepend tail of previous chunk
    final = []
    for i, chunk in enumerate(raw_chunks):
        if i > 0 and overlap > 0:
            prev_tail = raw_chunks[i - 1][-overlap:]
            chunk = prev_tail + chunk
        final.append(chunk.strip())

    return [c for c in final if c]


def annotate_chunks(
    chunks: list[str],
    regulations: dict = REGULATION_ANNOTATIONS,
) -> dict:
    """
    For each regulation, find which chunks are relevant using keyword matching.
    Returns {reg_label: {"relevant_chunk_indices": [...], "relevance_scores": [...], ...}}
    """
    annotations = {}

    for reg_label, reg_info in regulations.items():
        relevant_indices = []
        relevance_scores = []

        strong_patterns = [re.compile(p, re.IGNORECASE) for p in reg_info["strong"]]
        weak_patterns = [re.compile(p, re.IGNORECASE) for p in reg_info["weak"]]

        for idx, chunk in enumerate(chunks):
            strong_hits = sum(1 for p in strong_patterns if p.search(chunk))
            weak_hits = sum(1 for p in weak_patterns if p.search(chunk))

            if strong_hits > 0:
                relevant_indices.append(idx)
                relevance_scores.append(2)  # highly relevant
            elif weak_hits >= 2:
                # Need at least 2 weak matches to count as partially relevant
                relevant_indices.append(idx)
                relevance_scores.append(1)  # partially relevant

        annotations[reg_label] = {
            "relevant_chunk_indices": relevant_indices,
            "relevance_scores": relevance_scores,
            "description": reg_info["description"],
            "n_relevant": len(relevant_indices),
        }

    return annotations


def load_protocol_text(path: Path) -> tuple[str, str]:
    """Load protocol text and extract NCT ID from filename."""
    text = path.read_text(encoding="utf-8", errors="replace")
    # Extract NCT ID from filename pattern: NCTxxxxxxxx_...
    nct_match = re.search(r"(NCT\d{8})", path.stem)
    protocol_id = nct_match.group(1) if nct_match else path.stem
    return protocol_id, text


def generate_ground_truth(
    protocols_dir: Path,
    non_compliant_dir: Optional[Path] = None,
    output_path: Optional[Path] = None,
    limit: Optional[int] = None,
) -> list[dict]:
    """
    Generate ground truth annotations for all protocols.
    """
    samples = []

    # 1. Process compliant protocols
    if protocols_dir.exists():
        files = sorted(protocols_dir.glob("*.txt"))
        if not files:
            # Try PDF text extraction fallback
            files = sorted(protocols_dir.glob("*.pdf"))
            print(f"No .txt files found; found {len(files)} PDFs (parse them first with parse.py)")

        if limit:
            files = files[:limit]

        print(f"\n{'='*70}")
        print(f"GROUND TRUTH GENERATION — COMPLIANT PROTOCOLS")
        print(f"{'='*70}")
        print(f"Source: {protocols_dir.resolve()}")
        print(f"Protocols: {len(files)}")
        print()

        for f in files:
            protocol_id, text = load_protocol_text(f)
            chunks = chunk_text(text)
            annotations = annotate_chunks(chunks)

            # Summary
            total_relevant = sum(a["n_relevant"] for a in annotations.values())
            regs_with_hits = sum(1 for a in annotations.values() if a["n_relevant"] > 0)

            print(f"  {protocol_id:<20} chunks={len(chunks):<5} "
                  f"annotated_chunks={total_relevant:<4} "
                  f"regs_covered={regs_with_hits}/8")

            samples.append({
                "protocol_id": protocol_id,
                "source": "compliant",
                "source_file": str(f.name),
                "n_chunks": len(chunks),
                "chunks": chunks,
                "annotations": annotations,
            })

    # 2. Process non-compliant protocols (if available)
    if non_compliant_dir and non_compliant_dir.exists():
        nc_files = sorted(non_compliant_dir.glob("*.txt"))
        if limit:
            nc_files = nc_files[:limit]

        if nc_files:
            print(f"\n{'='*70}")
            print(f"GROUND TRUTH GENERATION — NON-COMPLIANT (REDACTED) PROTOCOLS")
            print(f"{'='*70}")
            print(f"Source: {non_compliant_dir.resolve()}")
            print(f"Protocols: {len(nc_files)}")
            print()

            for f in nc_files:
                protocol_id, text = load_protocol_text(f)
                chunks = chunk_text(text)
                annotations = annotate_chunks(chunks)

                total_relevant = sum(a["n_relevant"] for a in annotations.values())
                regs_with_hits = sum(1 for a in annotations.values() if a["n_relevant"] > 0)

                print(f"  {protocol_id:<20} chunks={len(chunks):<5} "
                      f"annotated_chunks={total_relevant:<4} "
                      f"regs_covered={regs_with_hits}/8")

                samples.append({
                    "protocol_id": f"{protocol_id}_redacted",
                    "source": "non_compliant",
                    "source_file": str(f.name),
                    "n_chunks": len(chunks),
                    "chunks": chunks,
                    "annotations": annotations,
                })

    # 3. Generate synthetic edge-case protocols (always available, no files needed)
    print(f"\n{'='*70}")
    print(f"GROUND TRUTH GENERATION — SYNTHETIC PROTOCOLS")
    print(f"{'='*70}")
    synthetic = _generate_synthetic_protocols()
    for s in synthetic:
        chunks = chunk_text(s["text"])
        annotations = annotate_chunks(chunks)
        total_relevant = sum(a["n_relevant"] for a in annotations.values())
        regs_with_hits = sum(1 for a in annotations.values() if a["n_relevant"] > 0)

        print(f"  {s['id']:<20} chunks={len(chunks):<5} "
              f"annotated_chunks={total_relevant:<4} "
              f"regs_covered={regs_with_hits}/8")

        samples.append({
            "protocol_id": s["id"],
            "source": "synthetic",
            "source_file": None,
            "n_chunks": len(chunks),
            "chunks": chunks,
            "annotations": annotations,
        })

    # Save
    if output_path:
        output_path.parent.mkdir(parents=True, exist_ok=True)
        # Save without full chunk text (too large); save indices and metadata only
        slim_samples = []
        for s in samples:
            slim = {k: v for k, v in s.items() if k != "chunks"}
            slim["chunk_previews"] = [c[:100] + "..." if len(c) > 100 else c for c in s["chunks"]]
            slim_samples.append(slim)

        with open(output_path, "w") as f:
            json.dump(slim_samples, f, indent=2)
        print(f"\nSaved annotations to {output_path.resolve()}")

        # Also save full version with chunks for the evaluator
        full_path = output_path.parent / "annotations_full.json"
        with open(full_path, "w") as f:
            json.dump(samples, f, indent=2)
        print(f"Saved full annotations (with chunks) to {full_path.resolve()}")

    # Summary statistics
    print(f"\n{'='*70}")
    print(f"SUMMARY")
    print(f"{'='*70}")
    print(f"Total samples:   {len(samples)}")
    print(f"  Compliant:     {sum(1 for s in samples if s['source'] == 'compliant')}")
    print(f"  Non-compliant: {sum(1 for s in samples if s['source'] == 'non_compliant')}")
    print(f"  Synthetic:     {sum(1 for s in samples if s['source'] == 'synthetic')}")
    print()

    # Per-regulation coverage
    print(f"{'Regulation':<25} {'Samples w/ hits':>15} {'Avg relevant chunks':>20}")
    print("-" * 65)
    for reg in REGULATION_ANNOTATIONS:
        samples_with = sum(
            1 for s in samples
            if s["annotations"].get(reg, {}).get("n_relevant", 0) > 0
        )
        avg_relevant = (
            sum(s["annotations"].get(reg, {}).get("n_relevant", 0) for s in samples)
            / max(len(samples), 1)
        )
        print(f"{reg:<25} {samples_with:>15} {avg_relevant:>20.1f}")

    return samples


