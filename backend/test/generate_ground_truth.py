"""
Generate ground truth annotations for CLARA retrieval evaluation.

This script creates structured ground truth data that maps each CFR regulation
to the specific protocol sections (chunks) that address it. This enables
measuring retrieval quality (Recall@k, Precision@k, MRR, NDCG) for each
embedding strategy.

Ground truth sources:
  1. REAL PROTOCOLS: Downloaded from ClinicalTrials.gov (data/documents/compliant/).
     Sections are annotated by keyword-matching against known regulatory topics,
     producing "silver-standard" labels. A human reviewer can upgrade these to
     gold-standard by editing the output JSON.

  2. SYNTHETIC NON-COMPLIANT: Generated by redacting required elements from real
     protocols (data/documents/non_compliant/). For these, the ground truth is
     that redacted sections should NOT match the corresponding regulation (negative
     examples), while un-redacted sections retain their original annotations.

Output:
  test/ground_truth/annotations.json — list of GroundTruthSample objects:
    {
      "protocol_id": "NCT03196180",
      "source": "compliant" | "non_compliant",
      "protocol_text": "...",
      "chunks": ["chunk_0", "chunk_1", ...],
      "annotations": {
        "21 CFR Part 50": {
          "relevant_chunk_indices": [2, 5, 14],
          "relevance_scores": [2, 1, 1],   // 2=highly relevant, 1=partially, 0=irrelevant
          "description": "Informed consent, vulnerable populations"
        },
        ...
      }
    }

Usage:
    python test/generate_ground_truth.py --protocols-dir data/documents/compliant/parsed
    python test/generate_ground_truth.py --protocols-dir data/documents/compliant/parsed --include-non-compliant
"""

